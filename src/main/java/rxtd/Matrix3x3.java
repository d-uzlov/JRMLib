package rxtd;

public class Matrix3x3 {
    public final double a11, a12, a13;
    public final double a21, a22, a23;

    public Matrix3x3(double a11, double a12, double a13, double a21, double a22, double a23) {
        this.a11 = a11;
        this.a12 = a12;
        this.a13 = a13;
        this.a21 = a21;
        this.a22 = a22;
        this.a23 = a23;
    }
}
